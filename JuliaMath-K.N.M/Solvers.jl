"""
    Solvers

Численные методы интегрирования систем ОДУ.

Все солверы принимают систему `sys`, начальное условие `u0 :: Vector{Float64}`,
временной интервал `tspan :: Tuple{Float64,Float64}` и шаг `dt`, и возвращают
кортеж `(t_steps, u_results, stats)`:

- `t_steps`   — вектор моментов времени
- `u_results` — вектор векторов состояния в каждый момент времени
- `stats`     — `SolverStats` с диагностикой (число вызовов производной, отклонённые шаги и т.д.)

Доступные методы по возрастанию точности:

| Функция          | Порядок    | Вызовов f/шаг |
|------------------|------------|---------------|
| `solve_euler`    | 1          | 1             |
| `solve_heun`     | 2          | 2             |
| `solve_rk4`      | 4          | 4             |
| `solve_adaptive` | 2 (адапт.) | 2 на попытку  |

Для сравнения всех методов на одной задаче: `compare_methods(sys, u0, tspan, dt)`.
"""
module Solvers

using ..Models
using Printf

export solve_euler, solve_heun, solve_rk4, solve_adaptive
export SolverStats, method_name, method_order
export compare_methods

# =============================================================================
# ВСПОМОГАТЕЛЬНАЯ СТРУКТУРА: статистика решения
#
# После каждого вызова солвера полезно знать:
#   - сколько раз вычислялась производная (мера вычислительной стоимости)
#   - сколько шагов было отклонено (только для адаптивного метода)
#   - фактический максимальный шаг (для адаптивного)
#   - фактический минимальный шаг (для адаптивного)
#
# Это позволяет сравнивать методы честно: не по времени (зависит от железа),
# а по числу вычислений производной — универсальная мера стоимости.
# =============================================================================

"""
    SolverStats

Диагностика, возвращаемая каждым солвером после интегрирования.

Поля:
- `n_derivative_calls` — суммарное число вызовов `get_derivative`; универсальная
                         мера вычислительной стоимости, не зависящая от железа
- `n_rejected_steps`   — число шагов, отклонённых адаптивным методом (у фиксированных методов = 0)
- `dt_min`             — наименьший фактически использованный шаг
- `dt_max`             — наибольший фактически использованный шаг
- `n_steps`            — итоговое число точек траектории
"""
struct SolverStats
    n_derivative_calls :: Int       # сколько раз вызвали get_derivative
    n_rejected_steps   :: Int       # сколько шагов отклонил адаптивный метод
    dt_min             :: Float64   # минимальный использованный шаг
    dt_max             :: Float64   # максимальный использованный шаг
    n_steps            :: Int       # итоговое число точек траектории
end

# Красивый вывод статистики в консоль
function Base.show(io::IO, s::SolverStats)
    println(io, "SolverStats:")
    println(io, "  Вызовов производной : $(s.n_derivative_calls)")
    println(io, "  Отклонённых шагов   : $(s.n_rejected_steps)")
    println(io, "  Шаг min / max       : $(round(s.dt_min, digits=6)) / $(round(s.dt_max, digits=6))")
    println(io, "  Точек траектории    : $(s.n_steps)")
end


# =============================================================================
# МЕТОД ЭЙЛЕРА (порядок 1)
#
# Формула: u[i+1] = u[i] + dt * f(u[i])
#
# Это первый член ряда Тейлора. Ошибка на шаге ~ O(dt²),
# глобальная ошибка ~ O(dt). Самый дешёвый: 1 вычисление f на шаг.
# Использовать только для демонстрации — в реальных задачах слишком неточен.
# =============================================================================

method_name(::Val{:euler})  = "Метод Эйлера"
method_order(::Val{:euler}) = 1

"""
    solve_euler(sys, u0, tspan, dt) -> (t_steps, u_results, stats)

Метод Эйлера первого порядка.

Формула: `u[i+1] = u[i] + dt · f(u[i])`

Один вызов производной на шаг. Глобальная ошибка O(dt). Самый быстрый,
но и наименее точный метод — подходит для демонстрации расхождения методов,
а не для серьёзных вычислений.
"""
function solve_euler(
    sys,
    u0     :: Vector{Float64},
    tspan  :: Tuple{Float64, Float64},
    dt     :: Float64
)
    t_start, t_end = tspan
    t_steps  = collect(t_start:dt:t_end)
    n_steps  = length(t_steps)

    u_results = Vector{Vector{Float64}}(undef, n_steps)
    u_results[1] = copy(u0)

    n_calls = 0   # счётчик вызовов производной

    for i in 1:(n_steps - 1)
        du = Models.get_derivative(sys, u_results[i])
        n_calls += 1

        u_results[i+1] = u_results[i] .+ dt .* du
    end

    stats = SolverStats(n_calls, 0, dt, dt, n_steps)
    return t_steps, u_results, stats
end


# =============================================================================
# МЕТОД ХОЙНА (RK2, порядок 2)
#
# Также известен как "улучшенный метод Эйлера" или трапециевидный метод.
#
# Идея: сделать предварительный шаг Эйлером (предиктор),
# затем скорректировать его, усреднив производные в начале и конце шага.
#
# k1 = f(u)              — производная в начале шага
# k2 = f(u + dt * k1)   — производная в конце (предсказанном Эйлером)
#
# u[i+1] = u[i] + dt/2 * (k1 + k2)
#
# Геометрический смысл: вместо касательной в одной точке берём среднее
# двух касательных — в начале и конце шага. Это интегрирование по правилу
# трапеции (отсюда второе название).
#
# Ошибка на шаге ~ O(dt³), глобальная ~ O(dt²).
# Стоимость: 2 вычисления f на шаг (вдвое дороже Эйлера).
# Точность: на порядок лучше Эйлера, на порядок хуже RK4.
# =============================================================================

method_name(::Val{:heun})  = "Метод Хойна (RK2)"
method_order(::Val{:heun}) = 2

"""
    solve_heun(sys, u0, tspan, dt) -> (t_steps, u_results, stats)

Метод Хойна второго порядка (RK2, «улучшенный Эйлер», правило трапеции).

```
k1 = f(u)
k2 = f(u + dt · k1)
u[i+1] = u[i] + dt/2 · (k1 + k2)
```

Два вызова производной на шаг. Глобальная ошибка O(dt²).
Геометрически: усредняет касательные в начале и в конце шага.
"""
function solve_heun(
    sys,
    u0     :: Vector{Float64},
    tspan  :: Tuple{Float64, Float64},
    dt     :: Float64
)
    t_start, t_end = tspan
    t_steps  = collect(t_start:dt:t_end)
    n_steps  = length(t_steps)

    u_results = Vector{Vector{Float64}}(undef, n_steps)
    u_results[1] = copy(u0)

    n_calls = 0

    for i in 1:(n_steps - 1)
        u_curr = u_results[i]

        # Шаг 1: предиктор (обычный Эйлер до конца шага)
        k1 = Models.get_derivative(sys, u_curr)
        n_calls += 1
        u_pred = u_curr .+ dt .* k1   # предсказанная точка

        # Шаг 2: корректор (производная в предсказанной точке)
        k2 = Models.get_derivative(sys, u_pred)
        n_calls += 1

        # Итоговый шаг: среднее двух производных (правило трапеции)
        u_results[i+1] = u_curr .+ (dt / 2.0) .* (k1 .+ k2)
    end

    stats = SolverStats(n_calls, 0, dt, dt, n_steps)
    return t_steps, u_results, stats
end


# =============================================================================
# МЕТОД РУНГЕ-КУТТА 4-го ПОРЯДКА (RK4)
#
# Четыре вычисления производной на шаг, взвешенное среднее по правилу
# Симпсона. Золотой стандарт для задач с умеренной точностью.
#
# k1 = f(u)                       — начало шага
# k2 = f(u + dt/2 * k1)          — середина (предсказание через k1)
# k3 = f(u + dt/2 * k2)          — середина (коррекция через k2)
# k4 = f(u + dt * k3)            — конец шага
#
# u[i+1] = u + dt/6 * (k1 + 2k2 + 2k3 + k4)
#
# Веса 1,2,2,1 — правило Симпсона: средние точки важнее крайних.
# Ошибка на шаге ~ O(dt⁵), глобальная ~ O(dt⁴).
# Стоимость: 4 вычисления f на шаг.
# =============================================================================

method_name(::Val{:rk4})  = "Рунге-Кутта 4-го порядка"
method_order(::Val{:rk4}) = 4

"""
    solve_rk4(sys, u0, tspan, dt) -> (t_steps, u_results, stats)

Классический метод Рунге–Кутта четвёртого порядка.

```
k1 = f(u)
k2 = f(u + dt/2 · k1)
k3 = f(u + dt/2 · k2)
k4 = f(u + dt · k3)
u[i+1] = u[i] + dt/6 · (k1 + 2k2 + 2k3 + k4)
```

Четыре вызова производной на шаг. Глобальная ошибка O(dt⁴).
Веса 1,2,2,1 соответствуют правилу Симпсона — средние точки шага важнее крайних.
Стандартный выбор для большинства задач с умеренными требованиями к точности.
"""
function solve_rk4(
    sys,
    u0     :: Vector{Float64},
    tspan  :: Tuple{Float64, Float64},
    dt     :: Float64
)
    t_start, t_end = tspan
    t_steps  = collect(t_start:dt:t_end)
    n_steps  = length(t_steps)

    u_results = Vector{Vector{Float64}}(undef, n_steps)
    u_results[1] = copy(u0)

    n_calls = 0

    for i in 1:(n_steps - 1)
        u_curr = u_results[i]

        k1 = Models.get_derivative(sys, u_curr)
        k2 = Models.get_derivative(sys, u_curr .+ (dt/2) .* k1)
        k3 = Models.get_derivative(sys, u_curr .+ (dt/2) .* k2)
        k4 = Models.get_derivative(sys, u_curr .+ dt .* k3)
        n_calls += 4

        u_results[i+1] = u_curr .+ (dt/6) .* (k1 .+ 2 .* k2 .+ 2 .* k3 .+ k4)
    end

    stats = SolverStats(n_calls, 0, dt, dt, n_steps)
    return t_steps, u_results, stats
end


# =============================================================================
# АДАПТИВНЫЙ МЕТОД (встроенная пара Эйлер/Хойн, порядок 1/2)
#
# Не путать с методом Дормана-Принса (RK45, порядок 4/5) — это более точная
# и более затратная пара, в данном проекте не реализована. Здесь используется
# минимальная встроенная пара для простоты и наглядности алгоритма
# адаптивного шага; для серьёзных задач лучше подойдёт RK45.
#
# ПРОБЛЕМА фиксированного шага:
#   В хаотических системах траектория бывает то плавной (нужен крупный шаг),
#   то резко изгибается (нужен мелкий шаг). Фиксированный dt — компромисс:
#   либо медленно (мелкий везде), либо неточно (крупный везде).
#
# ИДЕЯ адаптивного шага:
#   На каждом шаге вычислить решение ДВУМЯ методами разного порядка
#   и сравнить результаты. Разность = оценка локальной ошибки.
#   Если ошибка мала — увеличить шаг (траектория плавная).
#   Если ошибка велика — уменьшить шаг и повторить (резкий изгиб).
#
# МЕТОД ХОЙНА-ЭЙЛЕРА (встроенная пара 1-го/2-го порядка):
#   Мы используем упрощённую встроенную пару для наглядности:
#
#   k1 = f(u)
#   k2 = f(u + dt * k1)
#
#   u_low  = u + dt * k1              (Эйлер, порядок 1)
#   u_high = u + dt/2 * (k1 + k2)    (Хойн,  порядок 2)
#
#   error = ||u_high - u_low||        (норма разности = оценка ошибки)
#
# УПРАВЛЕНИЕ ШАГОМ:
#   Задаём допустимую ошибку tol (tolerance).
#   Если error < tol:  шаг принят, следующий шаг увеличить
#   Если error >= tol: шаг отклонён, уменьшить шаг и пересчитать
#
#   Формула изменения шага (стандартная):
#   dt_new = dt * min(f_max, max(f_min, safety * (tol/error)^0.5))
#
#   safety = 0.9 — запас надёжности (чтобы не балансировать на грани)
#   f_max  = 5.0 — максимальный коэффициент увеличения шага за раз
#   f_min  = 0.2 — минимальный коэффициент уменьшения шага за раз
#
# ПАРАМЕТРЫ:
#   tol     — допустимая локальная ошибка (по умолчанию 1e-6)
#   dt_init — начальный шаг
#   dt_min  — минимально допустимый шаг (защита от зависания)
#   dt_max  — максимально допустимый шаг
# =============================================================================

method_name(::Val{:adaptive})  = "Адаптивный RK (Хойн-Эйлер)"
method_order(::Val{:adaptive}) = 2   # эффективный порядок

"""
    solve_adaptive(sys, u0, tspan; tol, dt_init, dt_min, dt_max) -> (t_steps, u_results, stats)

Адаптивный метод с автоматическим выбором шага (пара Хойн–Эйлер).

На каждом шаге вычисляются два решения — первого порядка (Эйлер) и второго (Хойн).
Их разность служит оценкой локальной ошибки. Если ошибка укладывается в `tol`,
шаг принимается и следующий увеличивается; иначе — отклоняется и уменьшается.

Формула изменения шага:
```
dt_new = dt · clamp(0.9 · (tol/error)^0.5, 0.2, 5.0)
```

Параметры:
- `tol`     — допустимая локальная ошибка (по умолчанию `1e-6`)
- `dt_init` — начальный шаг (по умолчанию `0.01`)
- `dt_min`  — минимальный шаг; при достижении делается принудительный шаг Эйлером (по умолчанию `1e-8`)
- `dt_max`  — максимальный шаг (по умолчанию `1.0`)

Полезен, когда траектория чередует плавные и резкие участки — фиксированный шаг
в таких случаях либо медленный, либо неточный.
"""
function solve_adaptive(
    sys,
    u0      :: Vector{Float64},
    tspan   :: Tuple{Float64, Float64};
    tol     :: Float64 = 1e-6,
    dt_init :: Float64 = 0.01,
    dt_min  :: Float64 = 1e-8,
    dt_max  :: Float64 = 1.0
)
    t_start, t_end = tspan

    # Используем динамические массивы — размер траектории заранее неизвестен
    t_result = Float64[t_start]
    u_result = Vector{Float64}[copy(u0)]

    t       = t_start
    u       = copy(u0)
    dt      = dt_init

    n_calls    = 0
    n_rejected = 0
    dt_min_used = dt_init
    dt_max_used = dt_init

    # Константы управления шагом
    safety = 0.9   # запас надёжности
    f_max  = 5.0   # макс. увеличение шага за один раз
    f_min  = 0.2   # макс. уменьшение шага за один раз

    while t < t_end
        # Не перешагнуть конец интервала
        if t + dt > t_end
            dt = t_end - t
        end

        # --- Два решения: низкого и высокого порядка ---

        # k1 — производная в текущей точке
        k1 = Models.get_derivative(sys, u)
        n_calls += 1

        # k2 — производная в конце шага (по Эйлеру)
        u_euler = u .+ dt .* k1
        k2 = Models.get_derivative(sys, u_euler)
        n_calls += 1

        # Решение 1-го порядка (Эйлер)
        u_low  = u_euler

        # Решение 2-го порядка (Хойн)
        u_high = u .+ (dt / 2.0) .* (k1 .+ k2)

        # --- Оценка ошибки: евклидова норма разности ---
        # _norm() вычисляет sqrt(sum(x_i^2)) — длину вектора разности
        error = _norm(u_high .- u_low)

        # Защита от нулевой ошибки (деление на ноль)
        if error < 1e-15
            error = 1e-15
        end

        # --- Решение: принять шаг или отклонить ---
        if error <= tol
            # Шаг принят: обновляем состояние
            t = t + dt
            u = u_high   # берём более точное решение

            push!(t_result, t)
            push!(u_result, copy(u))

            # Запоминаем статистику шагов
            dt_min_used = min(dt_min_used, dt)
            dt_max_used = max(dt_max_used, dt)
        else
            # Шаг отклонён: пересчитаем с меньшим dt
            n_rejected += 1
        end

        # --- Вычисление нового шага ---
        # Формула: новый_dt = старый_dt * safety * (tol/error)^0.5
        # Смысл: если ошибка в 4 раза больше допустимой → шаг уменьшится вдвое
        scale = safety * (tol / error)^0.5
        scale = clamp(scale, f_min, f_max)   # ограничиваем коэффициент
        dt    = clamp(dt * scale, dt_min, dt_max)

        # Защита от зависания (если шаг стал минимальным — принудительно идём)
        if dt <= dt_min && t < t_end
            k1_safe = Models.get_derivative(sys, u)
            n_calls += 1
            u = u .+ dt_min .* k1_safe
            t = t + dt_min
            push!(t_result, t)
            push!(u_result, copy(u))
            dt = dt_init   # сбрасываем шаг до начального
        end
    end

    stats = SolverStats(n_calls, n_rejected, dt_min_used, dt_max_used, length(t_result))
    return t_result, u_result, stats
end


# =============================================================================
# УТИЛИТА: норма вектора (евклидово расстояние)
# Используется в solve_adaptive для оценки ошибки.
# Префикс "_" — функция внутренняя, не экспортируется.
# Имя выбрано так, чтобы не конфликтовать с LinearAlgebra.norm, если этот
# модуль будет когда-нибудь подключён рядом с `using LinearAlgebra`.
# _norm([a, b, c]) = sqrt(a² + b² + c²)
# =============================================================================

function _norm(v::Vector{Float64})
    return sqrt(sum(x^2 for x in v))
end


# =============================================================================
# УТИЛИТА: сравнение методов на одной задаче
#
# Запускает все четыре метода с одинаковыми параметрами и печатает таблицу.
# Удобно для демонстрации разницы в точности и стоимости.
#
# Использование:
#   sys = Models.LorenzSystem(10.0, 28.0, 8/3)
#   Solvers.compare_methods(sys, [1.0, 1.0, 1.0], (0.0, 10.0), 0.01)
# =============================================================================

"""
    compare_methods(sys, u0, tspan, dt)

Запускает все четыре метода с одинаковыми параметрами и печатает сравнительную таблицу.

Эталон точности — RK4 с шагом `dt/10`. Для каждого метода выводится:
число вызовов производной, число шагов траектории, ошибка в конечной точке
относительно эталона.

# Пример
```julia
sys = Models.LorenzSystem(10.0, 28.0, 8/3)
Solvers.compare_methods(sys, [1.0, 1.0, 1.0], (0.0, 10.0), 0.01)
```
"""
function compare_methods(sys, u0, tspan, dt)
    println("\n=== Сравнение методов ===")
    println("Система : $(Models.system_name(sys))")
    println("Интервал: $(tspan[1]) → $(tspan[2]),  dt = $dt\n")

    methods = [
        ("Эйлер  (порядок 1)", () -> solve_euler(sys, u0, tspan, dt)),
        ("Хойн   (порядок 2)", () -> solve_heun(sys, u0, tspan, dt)),
        ("RK4    (порядок 4)", () -> solve_rk4(sys, u0, tspan, dt)),
        ("RK адапт.         ", () -> solve_adaptive(sys, u0, tspan,
                                        tol=1e-6, dt_init=dt)),
    ]

    # Запускаем RK4 с мелким шагом как "эталон"
    _, u_ref, _ = solve_rk4(sys, u0, tspan, dt/10)

    println(@sprintf("%-22s %8s %10s %12s", "Метод", "Вызовов f", "Шагов", "Ошибка (конец)"))
    println("-"^58)

    for (name, solver) in methods
        t_res, u_res, stats = solver()

        # Ошибка: расстояние между конечными точками
        u_end     = u_res[end]
        u_end_ref = u_ref[end]
        err = _norm(u_end .- u_end_ref)

        println(@sprintf("%-22s %8d %10d %12.2e",
            name, stats.n_derivative_calls, stats.n_steps, err))
    end
    println()
