"""
    Models

Определения динамических систем для численного интегрирования.

Каждая система — структура с параметрами. Все системы реализуют единый интерфейс:

- `get_derivative(sys, u)` — вектор производных `du/dt` в точке `u`
- `system_name(sys)`       — человекочитаемое название
- `system_dimension(sys)`  — размерность фазового пространства
- `default_u0(sys)`        — начальные условия по умолчанию
- `default_params(Type)`   — параметры по умолчанию (возвращает именованный кортеж)

Благодаря единому интерфейсу солверы и анализ работают с любой системой
без изменений — достаточно добавить новую структуру и реализовать эти функции.
"""
module Models

export LorenzSystem, LotkaVolterraSystem, VanDerPolSystem, RosslerSystem, DoublePendulumSystem
export get_derivative, system_name, system_dimension, default_u0, default_params

# =============================================================================
# СИСТЕМА ЛОРЕНЦА
# Модель конвекции атмосферы (1963, Эдвард Лоренц)
# Уравнения:
#   dx/dt = σ(y - x)
#   dy/dt = x(ρ - z) - y
#   dz/dt = xy - βz
#
# Классические параметры: σ=10, ρ=28, β=8/3
# При этих значениях — детерминированный хаос, знаменитый аттрактор
# =============================================================================

"""
    LorenzSystem(sigma, rho, beta)

Система Лоренца — модель конвекции атмосферы (Эдвард Лоренц, 1963).

Поля:
- `sigma` — число Прандтля: отношение вязкости к теплопроводности
- `rho`   — число Рэлея: пропорционально разности температур верх/низ
- `beta`  — геометрический параметр ячейки конвекции

При классических значениях `sigma=10, rho=28, beta=8/3` система демонстрирует
детерминированный хаос — знаменитый «аттрактор-бабочка». Показатель Ляпунова ≈ 0.9.
"""
struct LorenzSystem
    sigma :: Float64   # число Прандтля (вязкость / теплопроводность)
    rho   :: Float64   # число Рэлея   (разность температур верх/низ)
    beta  :: Float64   # геометрия ячейки конвекции
end

"""
    get_derivative(sys::LorenzSystem, u) -> Vector{Float64}

Возвращает `[dx/dt, dy/dt, dz/dt]` для системы Лоренца в точке `u = [x, y, z]`.
"""
function get_derivative(sys::LorenzSystem, u::Vector{Float64})
    x, y, z = u[1], u[2], u[3]

    dx = sys.sigma * (y - x)
    dy = x * (sys.rho - z) - y
    dz = x * y - sys.beta * z

    return [dx, dy, dz]
end

system_name(::LorenzSystem)      = "Аттрактор Лоренца"
system_dimension(::LorenzSystem) = 3
default_u0(::LorenzSystem)       = [1.0, 1.0, 1.0]
default_params(::Type{LorenzSystem}) = (sigma=10.0, rho=28.0, beta=2.666)


# =============================================================================
# СИСТЕМА ЛОТКИ-ВОЛЬТЕРРЫ
# Модель хищник-жертва (Лотка 1920, Вольтерра 1926)
# Уравнения:
#   dx/dt =  αx - βxy    (жертвы: рост - гибель от хищников)
#   dy/dt = -γy + δxy    (хищники: гибель + размножение от жертв)
#
# Исправлен баг оригинала: было -y*y, стало -gamma*y
# Решение — замкнутые орбиты (периодические колебания, не хаос)
# =============================================================================

"""
    LotkaVolterraSystem(alpha, beta, gamma, delta)

Система Лотки–Вольтерры — модель «хищник–жертва» (Лотка 1920, Вольтерра 1926).

Поля:
- `alpha` — скорость роста популяции жертв при отсутствии хищников
- `beta`  — эффективность охоты: убыль жертв на одну встречу с хищником
- `gamma` — скорость гибели хищников при отсутствии пищи
- `delta` — эффективность питания: прирост хищников на одну встречу с жертвой

Решения — замкнутые орбиты (периодические колебания, хаоса нет).
Существует интеграл движения: `V = δx − γ ln x + βy − α ln y = const`.
Классические параметры: `alpha=1.0, beta=0.1, gamma=1.5, delta=0.075`.
"""
struct LotkaVolterraSystem
    alpha :: Float64   # скорость роста жертв
    beta  :: Float64   # эффективность охоты (убыль жертв на встречу)
    gamma :: Float64   # скорость гибели хищников без еды
    delta :: Float64   # эффективность питания (прирост хищников на встречу)
end

"""
    get_derivative(sys::LotkaVolterraSystem, u) -> Vector{Float64}

Возвращает `[dx/dt, dy/dt]`, где `x` — популяция жертв, `y` — хищников.
"""
function get_derivative(sys::LotkaVolterraSystem, u::Vector{Float64})
    x, y = u[1], u[2]

    # x — популяция жертв (зайцы)
    # y — популяция хищников (лисы)
    dx =  sys.alpha * x - sys.beta  * x * y   # жертвы: рост - потери
    dy = -sys.gamma * y + sys.delta * x * y   # хищники: потери + прирост

    return [dx, dy]
end

system_name(::LotkaVolterraSystem)      = "Хищник-жертва (Лотка-Вольтерра)"
system_dimension(::LotkaVolterraSystem) = 2
default_u0(::LotkaVolterraSystem)       = [10.0, 5.0]
default_params(::Type{LotkaVolterraSystem}) = (alpha=1.0, beta=0.1, gamma=1.5, delta=0.075)


# =============================================================================
# СИСТЕМА ВАН ДЕР ПОЛЯ
# Осциллятор с нелинейным затуханием (Бальтазар ван дер Поль, 1927)
# Уравнение второго порядка:
#   ẍ - μ(1 - x²)ẋ + x = 0
#
# Записываем как систему первого порядка (стандартный приём):
#   u[1] = x  (положение)
#   u[2] = y = dx/dt  (скорость)
#
#   dx/dt = y
#   dy/dt = μ(1 - x²)y - x
#
# Физический смысл члена μ(1 - x²):
#   при |x| < 1: (1-x²) > 0, затухание отрицательное → система НАКАЧИВАЕТСЯ
#   при |x| > 1: (1-x²) < 0, затухание положительное → система ТОРМОЗИТСЯ
# Это автоматически удерживает колебания на стабильном предельном цикле.
#
# Применения: модель сердечного ритма, радиогенераторы, нейронные осцилляторы
# При μ→0: почти гармонические колебания (синусоида)
# При μ>>1: релаксационные колебания (резкие скачки)
# =============================================================================

"""
    VanDerPolSystem(mu)

Осциллятор Ван дер Поля — нелинейный осциллятор с автостабилизацией амплитуды
(Бальтазар ван дер Поль, 1927).

Поля:
- `mu` — параметр нелинейности. При `mu → 0` колебания почти синусоидальные;
         при `mu >> 1` — релаксационные (резкие скачки напряжения).

Член `μ(1 − x²)` действует как адаптивное затухание:
при `|x| < 1` оно отрицательное (система накачивается), при `|x| > 1` — положительное
(тормозится). Результат — устойчивый предельный цикл при любых начальных условиях.

Применения: модели сердечного ритма, радиогенераторы, нейронные осцилляторы.
"""
struct VanDerPolSystem
    mu :: Float64   # параметр нелинейности (сила нелинейного затухания)
end

"""
    get_derivative(sys::VanDerPolSystem, u) -> Vector{Float64}

Возвращает `[dx/dt, dy/dt]`, где `u[1] = x` (положение), `u[2] = y = dx/dt` (скорость).
Уравнение второго порядка `ẍ − μ(1−x²)ẋ + x = 0` приведено к системе первого порядка.
"""
function get_derivative(sys::VanDerPolSystem, u::Vector{Float64})
    x = u[1]   # положение
    y = u[2]   # скорость (dx/dt)

    # Преобразование уравнения 2-го порядка в систему 1-го:
    dx = y
    dy = sys.mu * (1.0 - x^2) * y - x

    return [dx, dy]
end

system_name(::VanDerPolSystem)      = "Осциллятор Ван дер Поля"
system_dimension(::VanDerPolSystem) = 2
default_u0(::VanDerPolSystem)       = [2.0, 0.0]
default_params(::Type{VanDerPolSystem}) = (mu=1.0,)


# =============================================================================
# СИСТЕМА РОССЛЕРА
# Простейший хаотический аттрактор (Отто Росслер, 1976)
# Уравнения:
#   dx/dt = -y - z
#   dy/dt =  x + ay
#   dz/dt =  b + z(x - c)
#
# Классические параметры: a=0.2, b=0.2, c=5.7
#
# Росслер специально придумал эту систему как упрощение Лоренца.
# Механизм хаоса наглядный:
#   - в плоскости XY система делает спираль наружу (нестабильный фокус)
#   - когда x становится большим, член z(x-c) "выстреливает" z вверх
#   - большое z через уравнение dx/dt = -y-z "бросает" траекторию обратно
# Это складывание и растяжение траектории и порождает хаос.
#
# В отличие от Лоренца: аттрактор более "плоский", проще для анализа
# =============================================================================

"""
    RosslerSystem(a, b, c)

Аттрактор Росслера — простейший хаотический аттрактор (Отто Росслер, 1976).

Поля:
- `a` — скорость раскрутки спирали в плоскости XY
- `b` — масштаб выброса по оси Z
- `c` — порог нелинейного переключения

Механизм хаоса: траектория делает спираль наружу в плоскости XY до тех пор, пока
`x` не достигает порога `c`, после чего компонента `z` резко выбрасывается вверх
и «складывает» траекторию обратно к началу. Это растяжение и складывание порождает
хаос. В отличие от Лоренца, аттрактор более плоский и проще поддаётся анализу.
Классические параметры: `a=0.2, b=0.2, c=5.7`.
"""
struct RosslerSystem
    a :: Float64   # скорость раскрутки спирали
    b :: Float64   # масштаб выброса по z
    c :: Float64   # порог нелинейного переключения
end

"""
    get_derivative(sys::RosslerSystem, u) -> Vector{Float64}

Возвращает `[dx/dt, dy/dt, dz/dt]` в точке `u = [x, y, z]`.
"""
function get_derivative(sys::RosslerSystem, u::Vector{Float64})
    x, y, z = u[1], u[2], u[3]

    dx = -y - z
    dy =  x + sys.a * y
    dz =  sys.b + z * (x - sys.c)

    return [dx, dy, dz]
end

system_name(::RosslerSystem)      = "Аттрактор Росслера"
system_dimension(::RosslerSystem) = 3
default_u0(::RosslerSystem)       = [1.0, 1.0, 1.0]
default_params(::Type{RosslerSystem}) = (a=0.2, b=0.2, c=5.7)


# =============================================================================
# ДВОЙНОЙ МАЯТНИК
# Классический пример детерминированного хаоса в механике
#
# Две точечные массы m₁, m₂ на стержнях длиной L₁, L₂
# Обобщённые координаты: θ₁, θ₂ — углы от вертикали
#
# Состояние системы: u = [θ₁, ω₁, θ₂, ω₂]
#   θ — угол (рад), ω = dθ/dt — угловая скорость
#
# Уравнения выведены из лагранжевой механики.
# g = 9.81 м/с² — ускорение свободного падения (зафиксировано)
#
# При малых углах — почти периодично.
# При начальном угле > ~20° — быстро переходит в хаос.
# Чувствительность к начальным условиям максимальна из всех систем здесь.
# =============================================================================

"""
    DoublePendulumSystem(m1, m2, L1, L2)

Двойной маятник — классический пример детерминированного хаоса в механике.

Поля:
- `m1`, `m2` — массы первого и второго грузов (кг)
- `L1`, `L2` — длины первого и второго стержней (м)

Состояние системы: `u = [θ₁, ω₁, θ₂, ω₂]`, где `θ` — угол от вертикали (рад),
`ω = dθ/dt` — угловая скорость. Уравнения движения выведены из лагранжевой механики.

При малых начальных углах (< ~10°) поведение почти периодическое. При углах > ~20°
система быстро переходит в хаос — наиболее чувствительная к начальным условиям
система из всех, реализованных в этом проекте. Сохраняется полная механическая
энергия `E = T + U`, что позволяет контролировать точность интегрирования.
"""
struct DoublePendulumSystem
    m1 :: Float64   # масса первого груза (кг)
    m2 :: Float64   # масса второго груза (кг)
    L1 :: Float64   # длина первого стержня (м)
    L2 :: Float64   # длина второго стержня (м)
end

"""
    get_derivative(sys::DoublePendulumSystem, u) -> Vector{Float64}

Возвращает `[dθ₁/dt, dω₁/dt, dθ₂/dt, dω₂/dt]` из уравнений Эйлера–Лагранжа.
`g = 9.81 м/с²` зафиксировано внутри функции.
"""
function get_derivative(sys::DoublePendulumSystem, u::Vector{Float64})
    θ1, ω1, θ2, ω2 = u[1], u[2], u[3], u[4]

    m1, m2, L1, L2 = sys.m1, sys.m2, sys.L1, sys.L2
    g  = 9.81
    Δθ = θ2 - θ1   # разность углов

    # Вспомогательные выражения (из уравнений Лагранжа)
    cos_Δ  = cos(Δθ)
    sin_Δ  = sin(Δθ)
    M      = m1 + m2           # суммарная масса
    denom1 = L1 * (2*m1 + m2 - m2 * cos(2*Δθ))   # знаменатель для α1
    denom2 = L2 * (2*m1 + m2 - m2 * cos(2*Δθ))   # знаменатель для α2

    # Угловые ускорения (dω/dt) из уравнений Эйлера-Лагранжа
    α1 = (-g*(2*m1+m2)*sin(θ1)
          - m2*g*sin(θ1 - 2*θ2)
          - 2*sin_Δ*m2*(ω2^2*L2 + ω1^2*L1*cos_Δ)) / denom1

    α2 = (2*sin_Δ*(ω1^2*L1*M
          + g*M*cos(θ1)
          + ω2^2*L2*m2*cos_Δ)) / denom2

    # Производные вектора состояния
    dθ1 = ω1   # скорость = производная угла
    dω1 = α1   # ускорение = производная скорости
    dθ2 = ω2
    dω2 = α2

    return [dθ1, dω1, dθ2, dω2]
end

system_name(::DoublePendulumSystem)      = "Двойной маятник"
system_dimension(::DoublePendulumSystem) = 4
default_u0(::DoublePendulumSystem)       = [π/2, 0.0, π/2, 0.0]
default_params(::Type{DoublePendulumSystem}) = (m1=1.0, m2=1.0, L1=1.0, L2=1.0)


end # module Models