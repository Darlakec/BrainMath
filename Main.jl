# =============================================================================
# Main.jl — точка входа в приложение
#
# Запускает интерактивный симулятор динамических систем (см. Gui.jl).
# Использование:
#   julia --project=. Main.jl
# =============================================================================

include("Gui.jl")
using .Gui

println("Запуск симуляции...")

# Julia 1.12: после include() функция появляется в новой "версии мира" (world age).
# invokelatest заставляет интерпретатор использовать самую свежую версию функции,
# а не ту, что была видна на момент компиляции этого скрипта.
Base.invokelatest(Gui.run_app)

# display(fig) внутри run_app неблокирующий, поэтому это сообщение печатается
# сразу после того, как окно отрисовано и управление возвращено в REPL/скрипт.
println("Окно успешно открыто.")
